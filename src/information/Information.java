package information;

public class Information {
	private String id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	private String comunityname;
	private String logopath;
	private String adminor;
	private String num;
	private String resume;
	public String getComunityname() {
		return comunityname;
	}
	public void setComunityname(String comunityname) {
		this.comunityname = comunityname;
	}
	public String getLogopath() {
		return logopath;
	}
	public void setLogopath(String logopath) {
		this.logopath = logopath;
	}
	public String getAdminor() {
		return adminor;
	}
	public void setAdminor(String adminor) {
		this.adminor = adminor;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getResume() {
		return resume;
	}
	public void setResume(String resume) {
		this.resume = resume;
	}
	
}
