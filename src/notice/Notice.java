package notice;

public class Notice {
	private String noticetheme;
	private String comunity;
	private String time;
	private String location;
	private String more;
	public String getNoticetheme() {
		return noticetheme;
	}
	public void setNoticetheme(String noticetheme) {
		this.noticetheme = noticetheme;
	}
	public String getComunity() {
		return comunity;
	}
	public void setComunity(String comunity) {
		this.comunity = comunity;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getMore() {
		return more;
	}
	public void setMore(String more) {
		this.more = more;
	}
}
